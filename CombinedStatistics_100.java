	public static double var(int[] histo){
		double n=0;
		double avg=0;
		for (int i=0;i<histo.length;i++){
			avg+=histo[i]*i;
			n+=histo[i];
		}
		if(n>0){
			avg/=n;
		}
		double var=0;
		for (int i=0;i<histo.length;i++){
			var+=histo[i]*(i-avg)*(i-avg);
		}
		if(n>0){
			var/=n;
		}
		return var;
	}
	
